package com.renhejia.robot.commandlib.parser.tips;

/**
 * @author liujunbin
 */
public class Tips {

    private int code;
    private String msg;
    private TipsData data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public TipsData getData() {
        return data;
    }

    public void setData(TipsData data) {
        this.data = data;
    }

}
