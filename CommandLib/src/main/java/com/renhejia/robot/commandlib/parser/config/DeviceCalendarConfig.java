package com.renhejia.robot.commandlib.parser.config;

/**
 * @author liujunbin
 */
public class DeviceCalendarConfig {

    private int display_switch;
    private int audio_reminder_switch;
    private int source;

    public int getDisplay_switch() {
        return display_switch;
    }

    public void setDisplay_switch(int display_switch) {
        this.display_switch = display_switch;
    }

    public int getAudio_reminder_switch() {
        return audio_reminder_switch;
    }

    public void setAudio_reminder_switch(int audio_reminder_switch) {
        this.audio_reminder_switch = audio_reminder_switch;
    }

    public int getSource() {
        return source;
    }

    public void setSource(int source) {
        this.source = source;
    }




}
